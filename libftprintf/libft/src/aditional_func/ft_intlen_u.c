#include "../../inc/libft.h"

int		ft_intlen_u(size_t nb, int base)
{
	int		len;

	len = 0;
	if (nb <= 0)
		len = 1;
	while (nb != 0)
	{
		nb /= base;
		len++;
	}
	return (len);
}
